package org.bukkit.inventory;

/**
 * A item's rarity determines the default color of its name. This enum is
 * ordered from least rare to most rare.
 */
public enum ItemRarity {

    /**
     * White item name.
     */
    COMMON,
    /**
     * Yellow item name.
     */
    UNCOMMON,
    /**
     * Aqua item name.
     */
    RARE,
    /**
     * Light purple item name.
     */
    EPIC;
}
